class Solution {
public:
    /**
     * Runtime: 32 ms, faster than 94.61% of C++ online submissions for Contains Duplicate.
     * Memory Usage: 16.6 MB, less than 22.38% of C++ online submissions for Contains Duplicate.
     */
    bool containsDuplicate(vector<int>& nums) {
        unordered_map<int, int> m;
        for (int i = 0; i < nums.size(); ++i) {
            if (m[nums[i]]) return true;
            m[nums[i]]++;
        }
        return false;
    }
};


